package com.basics;

public class loops {
    public static void main(String[] args) {
        System.out.println("For loop Output!");
        for(int k=5; k>0;k-- ){
            System.out.println(k);}
        System.out.println("While loop Output!");
        int count =1;
        while(count<= 5){
            System.out.println(count);
            count++;
        }


    }
}
